  class Double_Node {
    /** @class Graph::Double_Node
    * @brief Helper class for creating an ordering for edge's nodes.
    */
    Node lo, hi;
    public:
      Double_Node(const Node n1, const Node n2) {
        if (n1 < n2) {
          lo = n1;
          hi = n2;
        } else {
          lo = n2;
          hi = n1;
        }
      }
      
      bool operator==(const Double_Node& nn) const {
        return (lo == nn.lo) && (hi == nn.hi);
      }

      bool operator<(const Double_Node& nn) const {
        if (lo < nn.lo) {
          return true;
        } else if ( (lo == nn.lo) && (hi < nn.hi) ) {
          return true;
        }
        return false;
      }
   };

  /** Finds index of edge in the look up map. 
   * If not found, returns -1. 
  **/
  int find_edge(const Node& a, const Node& b) const {
    const Double_Node nn = Double_Node(a,b);
    auto it = graph_edges_.find(nn);
    if (it == graph_edges_.end() )
      return -1;
    int idx = it->second;
    return idx;
  }

